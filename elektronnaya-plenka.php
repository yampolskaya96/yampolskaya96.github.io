				<h1><span>Электронная пленка</span></h1>
				<p> Одним из недорогих вариантов тонировки, которая позволяет избежать неприятностей с ДПС является установка электронной пленки для тонировки. Какие преимущества дает использование такой пленки?
<br><br>
Пленка способна менять свою прозрачность в зависимости от напряжения питания, поданного на нее. Смысл использования пленки заключается в том, что при обнаружении впереди сотрудников ДПС Вы делаете стекло прозрачным, а после того, как их проехали, восстанавливаете тонировку до необходимого уровня.
<br><br>
Выполняется это одним нажатием на кнопку, длительность процесса составляет чуть более минуты. Несомненным плюсом электронной тонировки является возможность регулирования светопроницаемости стекол в широком пределе и составляет от 85% до 5%. Это удобно для безопасного управления автомобилем в темное время суток.
<br><br>
Электрохромная пленка
<br><br>
        Пленка производится из полимеров, которые способны менять свою прозрачность и светопроницаемость когда к ним прикладывается напряжение питания. Данная пленка потребляет мало электричества, средний расход составляет 7 Вт на 1 кв. м.
<br><br>
        Электрохромная пленка даже в выключенном состоянии способна задержать вредный ультрафиолет до 90%. На рынке представлено несколько производителей, из них наиболее лучшего качества товар изготавливает американская компания ACP.
<br><br>
        Заинтересовались выпуском пленок и китайские производители. Наиболее схожа по характеристикам, но значительно дешевле продукция компании STD. Пленка сертифицирована и проходит все испытания ГИБДД.</p><br>
				<img class="responsive" src="img/audi101.jpg" alt="" />
				<p>Как сделать тонировку пленкой самостоятельно?</p>

       <p>Данную процедуру выполнить можно, но она потребует достаточно много сил. Прежде всего нужно приобрести пленку, которая продается в рулонах. Так же потребуются инструменты – канцелярский нож, салфетки, шпатели.
<br><br>
        Изначально требуется тщательно вымыть стекла изнутри и протереть насухо салфетками. Не допускается попадание пыли. Затем необходимо снять размеры, добавить припуск 8-10 мм с каждой стороны и вырезать заготовки. Далее с пленки снимается защитный слой, она прикладывается к стеклу и с помощью салфеток или шпателя разглаживается, начиная от середины к краям.
<br><br>
        После этого нужно обрезать лишнюю пленку, запитать ее от инвертора, который преобразует бортовое питание в нужное напряжение через регулятор. Провода следует хорошо заизолировать и спрятать под обшивку.
<br><br>
        Стоимость материала необходимого для тонирования передних двух боковых стекол составит порядка 50 тыс. рублей. Профессионалы выполняют данную работу за сумму от 80 тыс. рублей, но от них будет гарантия и правильно подобранные электронные компоненты. Как поступить, решать Вам.</p><br><br>
