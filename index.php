							</div>
							<div class="answer_item">
								<p class="question">Как работает система On-Glass?</p>
								<p class="answer"><span>Ответ:</span> Процесс тонирования начинается после подачи напряжения от края к центру стекла. Напряжение подается с помощью контроллера по токопроводящей шине, расположенной по его контуру. Контроллер программируется в зависимости от модели стекла.</p>
							</div>
