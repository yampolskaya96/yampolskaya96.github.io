								<p class="answer"><span>Ответ:</span> Стекла On-Glass являются точной копией стекол вашего автомобиля, но в отличие от них, изготовляются по эксклюзивной 3D технологии, благодаря которой есть возможность регулировать степень затемнения. Для их производства используется высококачественное стекло Pilkington.
<br>
Разработка этого уникального продукта началась в 1975 году. Сегодня автомобилистам предлагаются так называемые «умные» стекла, которые способны удовлетворить самые высокие требования.</p>
