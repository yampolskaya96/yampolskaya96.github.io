								<p class="answer"><span>Ответ:</span> На начальном этапе заключается договор купли-продажи. Далее производится предоплата. Оплатить товар можно через банк или непосредственно в офисе компании. Установка стекол производится специалистами компании в заранее договоренное время.</p>
