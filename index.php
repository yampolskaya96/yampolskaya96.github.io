							</div>
							
							<div class="answer_item">
								<p class="question">Что такое автомобильная Электро Тонировка?</p>
								<p class="answer"><span>Ответ:</span> Конструкция состоит из двух стекол, между которыми находится электрохромный полимер. Бронирующая пленка наклеивается с внутренней и внешней стороны стекла и обеспечивает дополнительную прочность и надежность конструкции. Токопроводящая шина располагается по контуру стекла.</p>
							</div>
							
							
							<div class="answer_item">
								<p class="question">Существуют ли аналоги стекла ON-Glass?</p>
								<p class="answer"><span>Ответ:</span> Аналогов стекла ON-Glass на сегодняшний день не существует. Зарубежная продукция имеет совершено другую технологию производства и принцип работы. В ее основе – жидкие кристаллы, которые меняют свое положение под воздействием напряжения. При наличии таких стекол видимость из салона перекрывается всего на 50%.</p>
							</div>
							
							
							<div class="answer_item">
								<p class="question">Как формируется стоимость комплекта стекол ON-Glass?</p>
								<p class="answer"><span>Ответ:</span> Цена зависит от сложности изделия. При ее формировании учитывается площадь стекла и радиус кривизны. В среднем комплект стекол обойдется от 100 до 230 тыс. рублей.</p>
