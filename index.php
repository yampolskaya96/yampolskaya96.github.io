							<div class="answer_item">
								<p class="question">Законность установки стекол ON-Glass</p>
								<p class="answer"><span>Ответ:</span> Уникальная технология производства стекол ON-Glass не имеет аналогов. Они прошли сертификацию и поэтому соответствуют ГОСТу. Кроме этого, использование таких стекол является абсолютно законным, так как они прошли проверку ГАИ и ГИБДД.</p>
							</div>
							
							<div class="answer_item">
								<p class="question">Особенность установки и использования стекол</p>
								<p class="answer"><span>Ответ:</span> Стекла устанавливаются вместо обычных, но при этом конструкция дверей остается прежней. Плавное изменение степени тонировки осуществляется с помощью специальной кнопки или регулятора.</p>
