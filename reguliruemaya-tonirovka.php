				<h1><span>Регулируемая тонировка</span></h1>
				<p>  Водители, которые привыкли ездить с тонировкой в полностью «зажаренном» автомобиле в полной мере ощутили сумму штрафов за нее и кучу потерянного времени при общении с сотрудниками ДПС. Некоторые, особо «счастливые» научились самостоятельно демонтировать тонировочную пленку прямо на дороге.
<br><br>
        Как быть если ездить в аквариуме не хочется? Вариант, который предлагают современные производители – электронная регулируемая тонировка. Что это такое? Давайте разберемся.
<br><br>
Принцип работы
<br><br>
        В основе системы лежат специально изготовленные на заводе боковые стекла для Вашего авто, внутри них размещен полимер, который под действием электрического тока способен менять свою светопроницаемость.
<br><br>
Кристаллы, расположенные внутри полимерного слоя, в зависимости от приложенного напряжения меняют свою способность пропускать световые лучи. При этом ультрафиолетовое и инфракрасное излучение гаситься с эффективностью до 90% даже на выключенной электронной системе. Плюсы перед обычной пленкой очевидны:
<br><br>
- Полимер находиться внутри стекла и не подвержен царапинам, загрязнению и прочим механическим нагрузкам;<br>
- В не рабочем состоянии стекло прозрачное, проходит все замеры ГИБДД;<br>
- Время, необходимое для полного затемнения составляет 60-90 секунд. При обнаружении сотрудников ДПС или при проезде стационарного поста тонировка отключается с кнопки или пульта дистанционного управления;<br>
- Предусмотрено регулирование степени тонирования от 85% до 12% (в ряде случаев до 4%);<br>
- На стекло, и выполненные работы дается гарантия.</p><br>

				<img class="responsive" src="img/mercedes103.jpg" alt="" />
				<p>Стекла изготавливаются на заводе и в точности соответствуют по размерам и кривизне штатным. Выпуск таких стекол сертифицирован, разрешена их эксплуатация на территории РФ.
<br><br>
Из электронной части для правильной работы устройства требуется инвертор и специальный регулятор, который обеспечивает плавное изменение величины напряжения. Практически все системы предусматривают дополнительное дублирование управления с дистанционного пульта, работающего на батарейках.
<br><br>
Потребляемая мощность устройства незначительная (около 7 Вт на 1 кв. м.), что позволяет использовать его на парковке при заглушенном моторе сроком на 2-4 часа. При монтаже системы меняются только стекла, все остальное остается заводским, что позволяет сохранить гарантию на машину от производителя.</p><br><br>
