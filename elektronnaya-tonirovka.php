				<h1><span>Электронная тонировка</span></h1>
				<p>Тонирование автомобиля обычной пленкой работает по схеме, когда свет от солнечных лучей задерживается материалом пленки, а оптический ход самих лучей не искажается. Система электронной тонировки автомобиля работает по этой же схеме.
<br><br>
В стекло встраивается специальная пленка, которая в зависимости от подаваемого на нее напряжение блокирует передачу солнечного света. Водитель, пользуясь электронной системой регулировки тока, может сделать стекло полностью прозрачным или же наоборот, снизить световой поток до 4%.
<br><br>
Встроенную электронную тонировку стекол автомобиля делают на заводах, которые занимаются производством автомобильных стекол. Инженеры, следуя инструкциям, подгоняют ее таким образом, чтобы в отключенном состоянии она подходила под все требования к автомобилю, а размеры стекла соответствовали родным стеклам.
<br><br>
В РФ официально разрешена продажа таких стекол, кроме того производитель предоставляет все необходимые сертификаты и результаты лабораторных испытаний. При установке электронной тонировки авто меняются только стекла, все остальные механизмы остаются заводскими, это позволяет сохранить гарантию на машину, если она новая.</p>
				<img class="responsive" src="img/bmw1.jpg" alt="" />
				<p>Степень затемнения стекла регулируется специальным электронным блоком, который в зависимости от модификации может менять светопроницаемость от 100% до 4%. Водители, тонировку, которая пропускает менее 5% света, называют глухой.
<br><br>
 Особенность системы заключается в том, что стекло не прозрачное до тех пор, пока на него подается напряжение питания. В случае, когда автомобиль находится на парковке в заглушенном состоянии долго поддерживать затемнение стекол не получиться, так как можно высадить АКБ.
<br><br>
Время оптимального сохранения затемнения составляет от 2 до 4 часов, после чего следует завести автомобиль для зарядки АКБ. Электронная тонировка делиться на две основные группы:
<br><br>
- PDLC-монохромная тонировка, которая использует для затемнения стекол жидкие кристаллы. Не получила широкого распространения вследствие того, что требует для питания напряжения в 100-120 Вольт и при отключении питания делает стекло непрозрачным;<br>
- Технология SPD – позволяет выполнять плавное изменение прозрачности стекла, при подаче напряжения через контроллер. При отключении напряжения стекло становиться прозрачным. Время полной тонировки занимает 60-90 секунд. Наиболее востребованная автомобилистами технология;</p>
