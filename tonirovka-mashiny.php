				<h1><span>Электронная тонировка машины</span></h1>
				<p>Законодательством РФ запрещена эксплуатация автомобилей с тонированными стеклами. За нарушение предусмотрен штраф или вариант устранения замечания на месте. Как быть тем, кто не хочет расставаться с тонировкой, но и платить штрафы, и тратить время на общение с инспекторами ДПС не намерен?
<br><br>
        Выходом из ситуации может стать электронная тонировка машины, которая выполняется по новой технологии разработанной американскими инженерами. В отключенном состоянии стекла такого автомобиля проходят все проверки ГИБДД, разрешены к эксплуатации на территории РФ и сертифицированы.
<br><br>
        Электронная тонировка подразумевает замену штатных стекол, как правило, двух передних боковых на изготовленные в заводских условиях стекла с полимерной пленкой внутри. Планируется внедрить в ближайшее время и лобовые стекла, выполненные по данной технологии, но они пока доступны для ограниченного модельного ряда.
<br><br>
        Под действием напряжения, которое подается на питающие шины расположенные в стекле через специальный регулятор-преобразователь, кристаллы внутри пленки изменяют свою светопропускную способность. В зависимости от величины напряжения этот показатель можно регулировать от 85% до 4%. На «включение» кристаллов необходимо 60-90 секунд.</p><br>
				<img class="responsive" src="img/mercedes105.jpg" alt="" />
				<p>Система такого тонирования не дешевая, в основном ее используют для установки на автомобили представительского класса. Так, самый дешевый вариант на два передних боковых стекла обойдется примерно в сумму от 80 тыс. рублей. Более продвинутая система, которая способна затемнить стекла до 4% и включает больше функций, обойдется в 250-300 тыс. рублей.
<br><br>
        В комплект входят стекла, электронная начинка, пульт дистанционного управления и гарантия. За монтаж системы придется дополнительно оплатить. При установке меняются только стекла, все остальные механизмы остаются от производителя. Электрические провода убираются за накладки, кнопки управления выносятся в удобное для водителя место.
<br><br>
        Производятся данные стекла за рубежом, в основном на американских заводах. Это гарантирует для клиента то, точное соответствие габаритных размеров оригиналам и соблюдение технологического процесса при производстве. Дополнительно дается гарантия от производителя.
<br><br>
        При необходимости стекла можно дополнительно бронировать специальной пленкой по разным технологиям, которые обеспечат ему большую механическую прочность и защитят от агрессивного воздействия окружающей среды.</p><br><br>
