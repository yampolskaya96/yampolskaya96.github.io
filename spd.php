				<h1><span>Электронная тонировка SPD технологии</span></h1>
				<p> Электронная тонировка – это современная технология, позволяющая беспрепятственно эксплуатировать автомобиль с тонированными стеклами, пересекать на нем границу и не получать штрафы от ГИБДД. Принятый закон о том, что тонировка запрещена не повод отказываться от комфорта затонированного автомобиля.
<br><br>
        Электронная тонировка SPD технологии один из самых удачных вариантов, который позволяет простым нажатием кнопки включить тонировку. Такие умные стекла можно эксплуатировать не только в автомобилях, но и других видах транспорта, в помещениях для выполнения зонирования.</p>

<p>SPD-технология</p>
<p>
        Конструкция выполняется из двух стекол, между которыми расположена специальная пленка из полимера содержащего взвешенные частицы. Стекла, задерживают ультрафиолет, инфракрасное излучение и придают необходимую жесткость изделию. Открыта данная технология была американскими исследователями.
<br><br>
        Принцип действия пленки основан на свойстве жидких кристаллов. При воздействии на них напряжения они выстраиваются в ряды, а в промежутки между ними свободно проникает световое излучение. Когда напряжение питания перестает действовать, то кристаллы в пленки принимают хаотичное расположение и не позволяют свету проникать внутрь.
<br><br>
Регулировка положения кристаллов возможна путем плавного изменения подаваемого напряжения. Минусом такой тонировки является то, что стекла изготавливаются заводским методом, что существенно влияет на стоимость установки.</p><br>
				<img class="responsive" src="img/bmw3.jpg" alt="" />
				<p>Развитие технологии</p>

       <p>Дальнейшее развитие технологии SPD повлекло возникновение умных систем тонировки с регулировкой светопроницаемости On Glass. Эти стекла устанавливаются на место штатных, точно копируя их геометрию и размеры, они разрешены к эксплуатации на территории РФ.
<br><br>
        Главной особенностью данной системы является то, что при отключении питания они становятся прозрачными. В электронных блоках предусмотрена возможность подачи напряжения на стекла при парковке с заглушенным двигателем. Длительность такого эффекта может достигать четырех часов.
<br><br>
        Изготовление стекол данного типа возможно на рамочные модели, как правило, ставят два передних боковых стекла. Постепенно внедряется производство лобовых стекол, но они доступны еще не для всех моделей. Стоимость интеграции данной системы составляет от 80 тыс. рублей.</p><br><br><br>
